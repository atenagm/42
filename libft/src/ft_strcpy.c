char	*ft_strcpy(char *dest, const char *src)
{
	char	*destionation;
	int		i;

	i = 0;
	destionation = dest;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
